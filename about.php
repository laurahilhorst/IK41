<p>
<h2><b>What makes Rhapsody different from other forums?</b></h2>
<br>
The main difference between Rhapsody and other forums is its layout. Contrary to traditional forums, Rhapsody gives you the opportunity to filter posts to their own liking. Using the drop-down menus in the side bar, you can select the subjects you want to read and/or talk about. Rhapsody uses cookies to remember these choices, so it can easily reproduce what you selected last time you visited the website.
</p>
<p>
Another thing that makes Rhapsody stand out is its audience. Whether you’re a metal guitarist, a drum and bass producer or a classical cellist, this forum provides you with the information you’re looking for and connects you to your peers. Rhapsody is especially suitable for people with multiple interests: it doesn’t make you pick a scene.
</p>
<p>
<h2><b>Isn’t the age of forums over?</b></h2>
<br>
To us, the age of forums was never over. While other media such as Facebook, Twitter and Instagram have mostly taken over the social scene, we firmly believe social media that are dedicated to a subject do a better job satisfying user’s needs than other media, which are usually cluttered with uninteresting nonsense. We know you don’t care about your aunt’s achievements in Facebook games and you only befriended her because you felt obliged to. With Rhapsody, you don’t have to. We’ve reinvented the forum to make it even more tailored to your likings. All we offer you is people and content you actually like.
